$myFileName = 'allergies';

$fileExt = '.csv';

$filename = $myFolder . $myFileName . $fileExt;

if ($debug) print '<p>filename is ' . $filename;

$file=fopen($filename, "r");

if($debug){
    if($file){
       print '<p>File Opened Successful.</p>';
    }
    else{
       print '<p>File Open Failed.</p>';
     }
}
if($file){
    if($debug) print '<p>Begin reading data into an array.</p>';

    // read the header row
    $headers[] = fgetcsv($file);

    if($debug) {
         print '<p>Finished reading headers.</p>';
         print '<p>My header array</p><pre>';
         print_r($headers);
         print '</pre>';
     }

     // read all the data
     while(!feof($file)){
         $allergies[] = fgetcsv($file);
     }

     if($debug) {
         print '<p>Finished reading data. File closed.</p>';
         print '<p>My data array<p><pre> ';
         print_r($allergies);
         print '</pre></p>';
     }
} // ends if file was opened 
fclose($file);

// print all of the available allergies
// as links in ordered list
print '<ol>';

foreach ($allergies as $allergy) {
    if ($allergy != []) {
        print '<li><a href="allergy-info.php?allergyType=' . $allergy[0] . '" target="_blank">' . $allergy[0] . '</a></li>';
    }
